int moveObjetoParaDireita(int posX, int largura){
    return posX + largura;
}
int moveObjetoParaEsquerda(int posX, int largura){
    return posX - largura;
}
int moveObjetoParaCima(int posY, int altura){
    return posY - altura;
}
int moveObjetoParaBaixo(int posY, int altura){
    return posY + altura;
}
